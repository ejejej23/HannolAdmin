package com.sp.schedule;

public class ShowStartTime {
	int sStartCode;
	String startTime;
	String note;
	
	public int getsStartCode() {
		return sStartCode;
	}
	public void setsStartCode(int sStartCode) {
		this.sStartCode = sStartCode;
	}
	public String getStartTime() {
		return startTime;
	}
	public void setStartTime(String startTime) {
		this.startTime = startTime;
	}
	public String getNote() {
		return note;
	}
	public void setNote(String note) {
		this.note = note;
	}
}
